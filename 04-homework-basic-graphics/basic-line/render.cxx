bool pixel_point::operator==(const pixel_point& point) {
  return x == point.x && y == point.y;
}

std::ostream& operator<<(std::ostream& os, const pixel_point& point) {
  os << "(x, y): " << point.x << ", " << point.y;
  return os;
}

